    public void addRole(EventObject e) {
        FramesetRole role = new FramesetRole();
        roleset.addRole(role);
        FramesetRoleJPanel framesetRoleJPanel = new FramesetRoleJPanel(langEnc);
        framesetRoleJPanel.setRole(role);
        framesetRoleJPanel.init();

        framesetRoleJPanel.setFramesetRolesetJPanel(this);

        rolesJPanel.add(framesetRoleJPanel);
    }

    public void removeRole(FramesetRoleJPanel framesetRoleJPanel) {
        FramesetRole role = framesetRoleJPanel.getRole();
        roleset.removeRole(role);

        rolesJPanel.remove(framesetRoleJPanel);

        setVisible(false);
        setVisible(true);
    }

    public void editExamples(EventObject e) {
        FramesetExamplesJPanel examplesJPanel = new FramesetExamplesJPanel(langEnc);

        if(owner != null)
            examplesJPanel.setOwner(owner);
        else if(dialog != null)
            examplesJPanel.setDialog(dialog);

        examplesJPanel.setRoleset(roleset);

        examplesJPanel.init();

        SanchayJDialog editJDialog = null;

        if(owner != null)
            editJDialog = new SanchayJDialog(owner, GlobalProperties.getIntlString("Edit_Examples"), true, (JPanelDialog) examplesJPanel);
        else if(dialog != null)
            editJDialog = new SanchayJDialog(dialog, GlobalProperties.getIntlString("Edit_Examples"), true, (JPanelDialog) examplesJPanel);

    	editJDialog.pack();

        int inset = 35;
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        editJDialog.setBounds(inset, inset,
		screenSize.width  - inset*5,
		screenSize.height - inset*4);

    	UtilityFunctions.centre(editJDialog);

        editJDialog.setVisible(true);
    }

