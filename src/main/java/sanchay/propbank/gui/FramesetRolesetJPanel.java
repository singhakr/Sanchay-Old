    public void init()
    {
        UtilityFunctions.setComponentFont(rolesetNoteJTextArea, langEnc);
        UtilityFunctions.setComponentFont(rolesNoteJTextArea, langEnc);

        String notes = getRoleset().getNote();

        if(notes != null)
            rolesetNoteJTextArea.setText(notes);

        String rolesNotes = getRoleset().getRolesNote();

        if(rolesNotes != null)
            rolesNoteJTextArea.setText(rolesNotes);
        
        FeatureStructure fs = getRoleset().getAttributes();

        if(fs != null)
        {
            FeatureValue fv = fs.getAttributeValue(GlobalProperties.getIntlString("name"));
            String name = "";

            if(fv != null)
                name = fv.getValue().toString();

            AttributeValueJPanel attributeValueJPanel = new AttributeValueJPanel(GlobalProperties.getIntlString("name"), name, langEnc);
            attributeValueJPanel.setFeatureStructure(fs);
            attribsJPanel.add(attributeValueJPanel);

            fv = fs.getAttributeValue(GlobalProperties.getIntlString("vncls"));
            String vncls = "";

            if(fv != null)
                vncls = fv.getValue().toString();

            attributeValueJPanel = new AttributeValueJPanel(GlobalProperties.getIntlString("vncls"), vncls, langEnc);
            attributeValueJPanel.setFeatureStructure(fs);
            attribsJPanel.add(attributeValueJPanel);
        }

        int count = getRoleset().countRoles();

        for (int i = 0; i < count; i++)
        {
            FramesetRole role = getRoleset().getRole(i);

            FramesetRoleJPanel framesetRoleJPanel = new FramesetRoleJPanel(langEnc);
            framesetRoleJPanel.setRole(role);
            framesetRoleJPanel.init();

            framesetRoleJPanel.setFramesetRolesetJPanel(this);

            rolesJPanel.add(framesetRoleJPanel);
        }

        setVisible(false);
        setVisible(true);
    }

